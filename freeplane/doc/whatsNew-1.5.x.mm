      Freeplane now supports scripts that are executed on startup. This is especially useful for scripts that change the behavior of Freeplane. Listeners that listen on events like opening maps, mouse or keyboard events are now possible! YIIIHAAA!
    </p>
    <ul>
      <li>
        Init scripts are all scripts in the directory &lt;freeplaneuserdir&gt;/scripts/init/.
      </li>
      <li>
        After startup (Freeplane window is up) all init scripts are run in lexicographical order of script names.(A to the Z)
      </li>
      <li>
        In an init script you can't rely on the variable node since it is not bound. You also should not assume that any script map is opened.
      </li>
    </ul>
